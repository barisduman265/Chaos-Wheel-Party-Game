/// Localized prompt text keyed by prompt id, per language.
///
/// English is the source of truth (lives in [PromptRepository]) and is always
/// the fallback. Each supported language provides eight parallel lists that
/// mirror the English lists in `prompt_repository.dart` *in the same order*.
/// Ids are rebuilt with the identical scheme the repository uses
/// (`<type>_<mode>_<NNN>`), so a translated list only needs to match order and
/// count — any entry that is missing simply falls back to English.
class PromptLocalizations {
  PromptLocalizations._();

  /// Returns the translated text for [id] in [language], or `null` to signal
  /// that the caller should fall back to the English source text.
  static String? textFor(String id, String language) =>
      _byLanguage[language]?[id];

  static final Map<String, Map<String, String>> _byLanguage = {
    'Turkish': _build(_turkish),
    'Spanish': _build(_spanish),
    'Italian': _build(_italian),
    'French': _build(_french),
    'Mandarin Chinese': _build(_chinese),
    'Japanese': _build(_japanese),
    'Korean': _build(_korean),
    'Portuguese': _build(_portuguese),
    'Arabic': _build(_arabic),
  };

  static Map<String, String> _build(_PromptSet set) {
    final map = <String, String>{};
    void add(PromptType type, PromptVibeMode mode, List<String> texts) {
      for (var i = 0; i < texts.length; i++) {
        final id =
            '${type.name}_${mode.name}_${(i + 1).toString().padLeft(3, '0')}';
        map[id] = texts[i];
      }
    }

    add(PromptType.truth, PromptVibeMode.cozy, set.cozyTruth);
    add(PromptType.dare, PromptVibeMode.cozy, set.cozyDare);
    add(PromptType.truth, PromptVibeMode.spicy, set.spicyTruth);
    add(PromptType.dare, PromptVibeMode.spicy, set.spicyDare);
    add(PromptType.truth, PromptVibeMode.unhinged, set.unhingedTruth);
    add(PromptType.dare, PromptVibeMode.unhinged, set.unhingedDare);
    add(PromptType.truth, PromptVibeMode.evil, set.evilTruth);
    add(PromptType.dare, PromptVibeMode.evil, set.evilDare);
    return map;
  }
}

