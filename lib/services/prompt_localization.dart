  const _PromptSet({
    required this.cozyTruth,
    required this.cozyDare,
    required this.spicyTruth,
    required this.spicyDare,
    required this.unhingedTruth,
    required this.unhingedDare,
    required this.evilTruth,
    required this.evilDare,
  });

  final List<String> cozyTruth;
  final List<String> cozyDare;
  final List<String> spicyTruth;
  final List<String> spicyDare;
  final List<String> unhingedTruth;
  final List<String> unhingedDare;
  final List<String> evilTruth;
  final List<String> evilDare;
}

